// Function to add 
function add(a, b) {
    return a + b;
}


// Function to subtract
function subtract(a, b) {
    return a - b;
}

// Function to multiply
function multiply (a, b) {
    return a * b;
}

// Function to divide
function divide(a, b) {
    if (b === 0) {
        return 'Error'
    } 
    return a / b;
}